package principal;

public class exercise_19 {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;

        if (a > b) {
            System.out.println("3 es mayor que 4");
        }else {
            System.out.println("3 no es mayor que 4");
        }

        if (a < b) {
            System.out.println("3 es menor que 4");
        }else {
            System.out.println("3 no es menor que 4");
        }

        if (a >= b) {
            System.out.println("3 es mayor que 4");
        }else {
            System.out.println("3 no es mayor que 4");
        }

        if (a <= b) {
            System.out.println("3 es mayor que 4");
        }else {
            System.out.println("3 no es mayor que 4");
        }

        if (a != b) {
            System.out.println("3 es mayor que 4");
        }else {
            System.out.println("3 no es mayor que 4");
        }
    }
}
